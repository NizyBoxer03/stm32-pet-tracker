void ParseGPSSentence(uint8_t *str, uint8_t *src, GPS_DATA* gps) //Funciona independientemente del método de buffer que terminemos usando
{
	for(int i = 0; i < BUF_SIZE; i++)
	{
		if(src[i] == '$')
		{
			for(int len = 0; src[i] == 0xD || len < NMEA0183_MAX_LENGTH; len++) //0xD = CR
			{
				str[len] = src[i]; //Copiar la línea del buffer al string que va a ser parseado
			}
		}

		//En este punto str ya debería contener una línea parseable
		if(strstr((char*)str, "RMC") != NULL) //Detecto RMC
		{
			const char s[3] = ",*";
			int counter = 0;
			char *param;

			param = strtok((char*)str, s);
			//$GPRMC

			param = strtok(NULL, s);

			if(*param != 'V')
			{
				while( param != NULL ) {

					switch(counter)
					{
						case 0: //UTC of pos (HHMMSS.SS)
							sscanf(param, "%2d%2d%f", &gps->hour, &gps->minute, &gps->second);
							break;

						case 1: //Validity (A/V)
							sscanf(param, "%c", &gps->valid);
							break;

						case 2: //lat (DDmm.mm)
							sscanf(param, "%2d%f", &gps->latdeg, &gps->latmin);
							break;

						case 3: //lat dir (S/N)
							sscanf(param, "%c", &gps->latdir);
							break;

						case 4: //long (DDDmm.mm)
							sscanf(param, "%3d%f", &gps->longdeg, &gps->longmin);
							break;

						case 5: //long dir (W/E)
							sscanf(param, "%c", &gps->longdir);
							break;

						case 6: //speed (knots) (x.x)
							sscanf(param, "%f", &gps->speed);
							break;

						case 7: //track true (?) (x.x)
							sscanf(param, "%f", &gps->tracktrue);
							break;

						case 8: //date (DDMMYY)
							sscanf(param, "%2d%2d%2d", &gps->day, &gps->month, &gps->year);
							break;

						case 9: //mag var (deg) (x.x)
							sscanf(param, "%f", &gps->magvar);
							break;

						case 10: //mag var dir (E/W) (sub/add)
							sscanf(param, "%c", &gps->magvardir);
							break;

						case 11: //mode ind (single char, see table)
							sscanf(param, "%c", &gps->mode);
							break;

						case 12: //Checksum
							sscanf(param, "%x", &gps->checksum);
							break;
					}

					param = strtok(NULL, s);


					counter++;
				}
			}

		}


	}
}

